public class switchCase {
    public static void main(String[] arg){
        int num = 3;
        switch (num) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Pizza");
                break;
            case 3:
                System.out.println("MOMOS");
                break;

            default:System.out.println("We wake up");
        }
    }
}
